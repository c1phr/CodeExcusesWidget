{
    dashcode.setupParts();
    getExcuse();
}

function getExcuse()
{
    var output = widget.system("curl -X GET 'http://www.codingexcuses.com/' -H 'Accept: text/plain'", null).outputString;
    document.getElementById('excuseText').innerHTML = output;
}

